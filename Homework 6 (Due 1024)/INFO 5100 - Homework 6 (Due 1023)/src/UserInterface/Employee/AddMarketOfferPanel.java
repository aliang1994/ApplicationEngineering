    private void NewButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewButActionPerformed
        MarketOffer mo = moc.addMarketOffer();
        
        Product p = (Product)comboBoxProduct.getSelectedItem();
        mo.setP(p);
        
        Market m =(Market) MktCombo.getSelectedItem();
        mo.setM(m);
            
        if( Double.parseDouble(txtFloor.getText()) > Double.parseDouble(txtTarget.getText()) || Double.parseDouble(txtFloor.getText()) > Double.parseDouble(txtCelling.getText()) || Double.parseDouble(txtCelling.getText())< Double.parseDouble(txtTarget.getText()) || Double.parseDouble(txtCelling.getText())< Double.parseDouble(txtFloor.getText())){
            JOptionPane.showMessageDialog(null, "Floor price<= Target price <= Celling price", "Info", JOptionPane.INFORMATION_MESSAGE);
        }else{
            mo.setFloorPrice(Double.parseDouble(txtFloor.getText()));
            mo.setTargetPrice(Double.parseDouble(txtTarget.getText()));
            mo.setCeilingPrice(Double.parseDouble(txtCelling.getText()));
            JOptionPane.showMessageDialog(null, "Added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }       
            
    }//GEN-LAST:event_NewButActionPerformed

