    public void populateSupplierTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblSupplier.getModel();
        dtm.setRowCount(0);
        for(Supplier s : sd.getList()){
           Object row[] = new Object[1];
            row[0] = s;
            dtm.addRow(row); 
        }
    }

