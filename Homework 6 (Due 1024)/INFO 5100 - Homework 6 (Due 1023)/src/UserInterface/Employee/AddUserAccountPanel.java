    private void NewButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewButActionPerformed
        // TODO add your handling code here:
        
        UserAccount ua = uad.addUser();
        ua.setUserName(txtUsername.getText());
        ua.setPassword(txtPassword.getText());
        ua.setRole(txtRole.getText());
        
        Person p = pd.addPerson();
        p.setName(txtName.getText());
        ua.setP(p);
        p.setUa(ua);
        
        JOptionPane.showMessageDialog(null, "Added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
        
        txtUsername.setText("");
        txtPassword.setText("");
        txtRole.setText("");
        txtName.setText("");
    }//GEN-LAST:event_NewButActionPerformed

