    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
        dtm.setRowCount(0);
        
        for(Order o: p.getOrderList()){
            Object[] row = new Object[3];
            row[0] = o;
            row[1] = o.getCustomer();
            row[2] = o.getTotalPrice();
            
            dtm.addRow(row);
        }
    }
    


