    private void ViewButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewButActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrder.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select Order");
        }else{
            Order o = (Order) tblOrder.getValueAt(selectedRow, 0);
            populateItems(o);
        }
    }//GEN-LAST:event_ViewButActionPerformed

    public void populateItems(Order o){
        DefaultTableModel dtm = (DefaultTableModel) tblOrderItem.getModel();
        dtm.setRowCount(0);
        
        for(OrderItem oi: o.getList()){
            Object[] row = new Object[4];
            row[0] = oi;
            row[1] = oi.getP().getId();
            row[2] = oi.getQuantity();
            row[3] = oi.getSalesPrice();
            
            dtm.addRow(row);
        }
    }

