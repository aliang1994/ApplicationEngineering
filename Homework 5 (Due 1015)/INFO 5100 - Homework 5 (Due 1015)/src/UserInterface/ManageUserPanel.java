    private void UpdateButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButActionPerformed
        int row = DisplayTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
        }
        UserAccount ua = (UserAccount)DisplayTable.getValueAt(row, 0);
        
        UpdateUserPanel uup = new UpdateUserPanel(b, panel, ua);
        panel.add("UpdateUserPanel", uup);
        CardLayout lo = (CardLayout) panel.getLayout();
        lo.next(panel);
    }//GEN-LAST:event_UpdateButActionPerformed

