    private void CreateButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateButActionPerformed
        UserAccount ua = b.getUad().addAccount();
        ua.setP((Person)PersonCombo.getSelectedItem());
        ua.setRole((String)RoleCombo.getSelectedItem());
        ua.setUsername(UserTxt.getText());
        ua.setPassword(PassTxt.getText());
        
        JOptionPane.showMessageDialog(null,"New User Successfully Created");
        UserTxt.setText("");
        PassTxt.setText("");
    }//GEN-LAST:event_CreateButActionPerformed

    private void BackButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackButActionPerformed
        panel.remove(this);
        Component[] array = panel.getComponents();
        Component comp = array[array.length-1];
        ManageUserPanel mup = (ManageUserPanel)comp;
        mup.populateTable();
        
        CardLayout layout = (CardLayout) panel.getLayout();
        layout.previous(panel);
    }//GEN-LAST:event_BackButActionPerformed

