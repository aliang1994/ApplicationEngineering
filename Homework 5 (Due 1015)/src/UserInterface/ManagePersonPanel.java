    public void populateTable(){
        dtm = (DefaultTableModel)DisplayTable.getModel();        
        dtm.setRowCount(0);  
        
        for(Person p: b.getPd().getList()){
            Object[] row = new Object[3];
            row[0] = p;
            row[1] = p.getAge();
            row[2] = p.getList();
            
            dtm.addRow(row);
        }            
    }

