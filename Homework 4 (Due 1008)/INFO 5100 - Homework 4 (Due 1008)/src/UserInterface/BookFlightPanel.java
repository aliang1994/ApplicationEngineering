    private void bookFlight(){
        ArrayList<Seat> seatlist = fChosen.getAvailSeat();
        for (Seat s: seatlist){
            if(s.isAvail()){
                s.setAvail(false);
                break;                
            }
        } 
    }
    
    private void BookButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookButActionPerformed
        ArrayList<Seat> seatlist = fChosen.getAvailSeat();
        if(seatlist.size()==0){
            JOptionPane.showMessageDialog(null, "This flight is full. Please choose another one");            
        }
        bookFlight();        
        JOptionPane.showMessageDialog(null, "Seat reserved on this flight");
        SeatTxt.setText(Integer.toString(seatlist.size()));
    }//GEN-LAST:event_BookButActionPerformed


