    private void populateTable(){
        dtm = (DefaultTableModel)AirlinerTable.getModel();
        dtm.setRowCount(0);
        
        for(Airliner air: ad.getAirliner()){
            Object row[] = new Object[2];
                          
            row[0] = air;
            row[1] = air.getDateCreated();
           
            dtm.addRow(row);
        }
    }

