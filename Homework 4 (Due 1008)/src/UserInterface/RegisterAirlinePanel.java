    private void NewButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NewButActionPerformed
        Airliner air = ad.addAirliner();
        CreateAirlinerPanel cap = new CreateAirlinerPanel(ms,ad,cd, CardPanel,air);
        CardPanel.add("CreateAirlinerPanel", cap);
        CardLayout lo = (CardLayout) CardPanel.getLayout();
        lo.next(CardPanel);
    }//GEN-LAST:event_NewButActionPerformed

    private void ViewButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewButActionPerformed
        int selectedRow = AirlinerTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"please select a row");
        }
        else{
            Airliner air = (Airliner) AirlinerTable.getValueAt(selectedRow, 0);
            ViewAirlinerPanel vap = new ViewAirlinerPanel(ms,ad,cd, CardPanel, air);
            
            CardPanel.add("ViewAirlinerPanel", vap);
            CardLayout lo = (CardLayout) CardPanel.getLayout();
            lo.next(CardPanel);           
            
            vap.setNameTxt(air.getName());            
            vap.setDateTxt(air.getDateCreated());
        }        
    }//GEN-LAST:event_ViewButActionPerformed
   
    private void ManageFlightButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ManageFlightButActionPerformed
        int selectedRow = AirlinerTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"please select a row");
        }
        else{
            Airliner air = (Airliner) AirlinerTable.getValueAt(selectedRow, 0);            
            
            ManageFlightPanel mfp = new ManageFlightPanel(ms,ad,cd, CardPanel, air);            
            CardPanel.add("UpdateFlightPanel", mfp);
            CardLayout lo = (CardLayout) CardPanel.getLayout();
            lo.next(CardPanel);             
        }       
    }//GEN-LAST:event_ManageFlightButActionPerformed

