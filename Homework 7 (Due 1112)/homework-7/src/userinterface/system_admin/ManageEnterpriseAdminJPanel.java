    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblEnterpriseAdmin.getSelectedRow();

        if (selectedrow >= 0) {
            UserAccount ua = (UserAccount) tblEnterpriseAdmin.getValueAt(selectedrow, 0);
            Enterprise e = (Enterprise) tblEnterpriseAdmin.getValueAt(selectedrow, 1);
            e.getUserAccountDirectory().getUserAccountList().remove(ua);
            JOptionPane.showMessageDialog(null, "Deleted");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblEnterpriseAdmin.getSelectedRow();

        if (selectedrow >= 0) {
        UserAccount ua = (UserAccount) tblEnterpriseAdmin.getValueAt(selectedrow, 0);
        State s =(State) tblEnterpriseAdmin.getValueAt(selectedrow, 2);
        UpdateEnterpriseAdminJPanel updateEnterpriseAdminJPanel = new UpdateEnterpriseAdminJPanel(s, ua, userProcessContainer);
        userProcessContainer.add("updateEnterpriseAdminJPanel", updateEnterpriseAdminJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


