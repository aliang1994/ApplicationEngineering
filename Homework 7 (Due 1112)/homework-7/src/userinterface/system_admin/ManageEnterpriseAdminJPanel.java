    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().length() < 1 || String.valueOf(txtPassword.getPassword()).length() < 1 || txtUserName.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Please complete blanks", "Error", ERROR_MESSAGE);
            return;
        }
        Enterprise enterprise = (Enterprise) cbEnterprise.getSelectedItem();
        if (enterprise == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String username = txtUserName.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String emploeeName = txtName.getText();

        Employee employee = enterprise.getEmployeeDirectory().createEmployee(emploeeName);

        UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
        populateTable();
    }//GEN-LAST:event_btnSubmitActionPerformed

